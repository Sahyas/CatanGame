public class Location {
    private final int xCoord;
    private final int yCoord;

    public Location(int xCoord, int yCoord) {
        this.xCoord = xCoord;
        this.yCoord = yCoord;
    }

    public int getxCord() {
        return xCoord;
    }

    public int getyCord() {
        return yCoord;
    }
}
